package netOrcsShared;

import java.awt.Point;

public class Orc {
	private Point position;
	private int index;
	public Orc(){
		
	}
	public Point getPosition() {
		return position;
	}
	public void setPosition(Point position) {
		this.position = position;
	}
	public int getIndex() {
		return index;
	}
	public void setIndex(int index) {
		this.index = index;
	}

	
}
